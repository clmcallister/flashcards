create table TOPICS (
	TOPIC_ID INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
	NAME VARCHAR(100)
);

create table FLASH_CARD (
 FLASH_CARD_ID BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
 QUESTION VARCHAR(100),
 ANSWER VARCHAR(100),
 TOPIC_ID INT);
